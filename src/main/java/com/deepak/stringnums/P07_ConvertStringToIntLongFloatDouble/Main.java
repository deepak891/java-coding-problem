package com.deepak.stringnums.P07_ConvertStringToIntLongFloatDouble;

public class Main {
    public static void main(String[] args) {
        //Convert String to Integer/int
        Integer toInt1 = Integer.valueOf("123");
        int toInt2 = Integer.parseInt("123");

        //Convert String to long/Long
        Long toLong1 = Long.valueOf("123");
        long toLong2 = Long.parseLong("123");

        //Convert String to float/Float
        Float toFloat = Float.valueOf("123");
        float toFloat2 = Float.parseFloat("12.34");
    }
}
